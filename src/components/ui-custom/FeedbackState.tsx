import { Icon, StackProps, VStack } from "@chakra-ui/react";
import P from "./P";
import { IconDatabaseOff } from "@tabler/icons-react";

interface Props extends StackProps {
  icon?: any;
  title?: any;
  description?: any;
}

const FeedbackState = (props: Props) => {
  // Props
  const { icon, title, description, children, ...restProps } = props;

  // States
  const titleString = typeof title === "string";
  const descriptionString = typeof description === "string";

  return (
    <VStack gap={1} {...restProps}>
      <Icon mb={2} color={"fg.subtle"} boxSize={10}>
        {icon || <IconDatabaseOff />}
      </Icon>

      {titleString && (
        <P textAlign={"center"} fontSize={"lg"} fontWeight={"semibold"}>
          {title}
        </P>
      )}

      {!titleString && title}

      {descriptionString && (
        <P textAlign={"center"} color={"fg.subtle"}>
          {description}
        </P>
      )}

      {!descriptionString && description}

      {children}
    </VStack>
  );
};

export default FeedbackState;
